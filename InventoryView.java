    private void MoveObjectsButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_MoveObjectsButtonActionPerformed

        int piecesMoved = Integer.parseInt(PiecesMovedBox.getText());
        
        if(piecesMoved <= BackPcs && piecesMoved > 0 || piecesMoved * -1 <= ShelfPcs && piecesMoved < 0){
            try {
                Controller.moveBackstock(MovementList.getSelectedIndex(), PiecesMovedBox.getText());
            } catch (IOException ex) {
                Logger.getLogger(InventoryView.class.getName()).log(Level.SEVERE, null, ex);
            }
BackAmtLabel.setText("Number in Back: ");
ShelfAmtLabel.setText("Number on Shelf: ");
EmptyShelfLabel.setText("Empty Shelf Space: ");
PerCaseLabel.setText("Pieces per Case: ");
        }else{
            BackAmtLabel.setText("more pieces");
ShelfAmtLabel.setText("Cannot move");
EmptyShelfLabel.setText("than owned!!");
PerCaseLabel.setText("**********");
            
        }
    }//GEN-LAST:event_MoveObjectsButtonActionPerformed

