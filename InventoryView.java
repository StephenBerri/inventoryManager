    private void MovementListValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_MovementListValueChanged

       if (MovementList.getSelectedIndex() > -1){ 

        int selected = MovementList.getSelectedIndex();
String ProdString = Controller.lookupProduct(selected);
String[] prod = ProdString.split("`");
//split the returned string and assign the parts to the correct text feild.
BackAmtLabel.setText("Number in Back: " + prod[5]);
PerCaseLabel.setText("Pieces per Case: " + prod[6]);
//UpDesc.setText(prod[1]);
//UpID.setText(prod[0]);
//UpPrice.setText(prod[2]);
ShelfAmtLabel.setText("Number on Shelf: " + prod[4]);
//UpShelfSpace.setText(prod[3]);
BackPcs = Integer.parseInt(prod[5]);
ShelfPcs = Integer.parseInt(prod[4]);
int openShelf = Integer.parseInt(prod[3]) - Integer.parseInt(prod[4]);

EmptyShelfLabel.setText("Empty Shelf Space: " + openShelf);

if(openShelf > Integer.parseInt(prod[5])){
    PiecesMovedBox.setText(prod[5]);
}else{
    PiecesMovedBox.setText(Integer.toString(openShelf));
}

       }

    }//GEN-LAST:event_MovementListValueChanged
