    private void EditProdBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditProdBtnActionPerformed
//gets the selection of the list, then clears it
        int selected = ProductList.getSelectedIndex();
ProductList.clearSelection();
//makes sure something is selected
if(selected == -1){
    UpdateWarningLabel.setText("Please select a product to update from the list.");
}else{if(UpID.getText().equals("") || UpDesc.getText().equals("") || UpPrice.getText().equals("") || UpShelfSpace.getText().equals("") || UpShelf.getText().equals("") || UpBack.getText().equals("") || UpCaseSize.getText().equals("")){
          UpdateWarningLabel.setText("Please complete all feilds.");    
}else{
    try {//calls the update function in the controller script
        Controller.updateDatabase(selected, UpID.getText(), UpDesc.getText(), UpPrice.getText(), UpShelfSpace.getText(), UpShelf.getText(), UpBack.getText(), UpCaseSize.getText());
    } catch (IOException ex) {
        Logger.getLogger(InventoryView.class.getName()).log(Level.SEVERE, null, ex);
    }
    
}}


    }//GEN-LAST:event_EditProdBtnActionPerformed

    private void ProductListValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_ProductListValueChanged
        // make sure a product is selected in the list, call the function productlookup
       if (ProductList.getSelectedIndex() > -1){ 
      UpdateWarningLabel.setText("");
        int selected = ProductList.getSelectedIndex();
String ProdString = Controller.lookupProduct(selected);
String[] prod = ProdString.split("`");
//split the returned string and assign the parts to the correct text feild.
UpBack.setText(prod[5]);
UpCaseSize.setText(prod[6]);
UpDesc.setText(prod[1]);
UpID.setText(prod[0]);
UpPrice.setText(prod[2]);
UpShelf.setText(prod[4]);
UpShelfSpace.setText(prod[3]);
       }
    }//GEN-LAST:event_ProductListValueChanged

